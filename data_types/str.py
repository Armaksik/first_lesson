str = "hello world"

print(str.upper())
print(str.lower()) # преобразует в верхний/нижний регистр.

print(str.strip()) # удаляет пробелы с краёв.

print(str.split()) # разбивает строку по разделителю.

print(str.join()) # объединяет элементы через строку.

print(str.replace()) # заменяет подстроку.

print(str.find()) # ищет подстроку (возвращает индекс или -1).

print(str.startswith()), str.endswith() # проверяет начало/конец строки.

print(str.isdigit())
print(str.isalpha()) # проверяет, состоит ли строка из цифр/букв
